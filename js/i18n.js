let currentLang = 'en';

function setLanguage(lang) {
    currentLang = lang;
    document.documentElement.setAttribute('data-lang', lang);
    document.documentElement.setAttribute('lang', lang);

    const langToggle = document.getElementById('langToggle');
    const labels = langToggle.querySelectorAll('.lang-label');
    labels.forEach(label => {
        label.removeAttribute('data-lang-active');
        if (label.textContent.trim() === lang.toUpperCase()) {
            label.setAttribute('data-lang-active', '');
        }
    });

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[lang] && translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });

    if (translations[lang] && translations[lang]['meta.title']) {
        document.title = translations[lang]['meta.title'];
    }
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc && translations[lang] && translations[lang]['meta.description']) {
        metaDesc.setAttribute('content', translations[lang]['meta.description']);
    }

    localStorage.setItem('soullens-lang', lang);
}

document.addEventListener('DOMContentLoaded', () => {
    const saved = localStorage.getItem('soullens-lang');
    if (saved && translations[saved]) {
        setLanguage(saved);
    }

    document.getElementById('langToggle').addEventListener('click', () => {
        setLanguage(currentLang === 'en' ? 'de' : 'en');
    });
});
